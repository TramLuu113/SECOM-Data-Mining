## Define the function to fit the test set on the trained model and perform evalutation
def evaluate_model(model, X_train, y_train, X_test, y_test):

    # Fit the model to the training data
    model.fit(X_train, y_train)

    # Make predictions on the test data
    y_pred = model.predict(X_test)

    # Calculate accuracy score
    accuracy = accuracy_score(y_test, y_pred)

  # Calculate confusion matrix
    cm = confusion_matrix(y_test, y_pred)

    # Calculate precision score
    precision = precision_score(y_test, y_pred)

    # Calculate recall score
    recall = recall_score(y_test, y_pred)

    # Calculate true negatives (TN)
    TN = cm[0, 0]

    # Calculate false positives (FP)
    FP = cm[0, 1]

    # Calculate false negatives (FN)
    FN = cm[1, 0]

    # Calculate true positives (TP)
    TP = cm[1, 1]

    # Calculate F1 score
    f1 = f1_score(y_test, y_pred)

    # Calculate F2 score
    f2 = fbeta_score(y_test, y_pred, beta=2)

    # Calculate AUC score
    auc = roc_auc_score(y_test, y_pred)

    # Generate ROC curve
    fpr, tpr, thresholds = roc_curve(y_test, y_pred)

    # Plot ROC curve
    plt.plot(fpr, tpr)
    plt.xlabel('False Positive Rate')
    plt.ylabel('True Positive Rate')
    plt.title('ROC Curve')
    plt.show()

    # Plot confusion matrix
    plt.figure(figsize=(8, 6))
    sns.heatmap(cm, annot=True, fmt='d', cmap='Blues', center=0.5)
    plt.title('Confusion Matrix')
    plt.xlabel('Predicted Labels')
    plt.ylabel('True Labels')

    # Set x-axis and y-axis tick labels
    plt.xticks([0.5, 1.5], ['Pass', 'Fail'])
    plt.yticks([0.5, 1.5], ['Pass', 'Fail'])

    plt.show()

    # Return evaluation results
    evaluation_results = {
        'Accuracy': accuracy,
        'Confusion Matrix': cm,
        'Precision': precision,
        'Recall': recall,
        'F1 Score': f1,
        'F2 Score': f2,
        'AUC Score': auc,
        'TN': TN,
        'FP': FP,
        'FN': FN,
        'TP': TP
    }

    print(evaluation_results)

